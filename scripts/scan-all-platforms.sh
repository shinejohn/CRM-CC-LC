#!/bin/bash

# Scan All Platforms Script
# Runs comprehensive scans across all Fibonacco platforms

set -e

GREEN='\033[0;32m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
BLUE='\033[0;34m'
NC='\033[0m'

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(dirname "$SCRIPT_DIR")"

echo -e "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
echo -e "${BLUE}🔍 Scanning All Fibonacco Platforms${NC}"
echo -e "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
echo ""

cd "$PROJECT_ROOT"

# List of platforms to scan
PLATFORMS=(
    "learning-center"
    # Add other platforms when locations are configured
    # "task-juggler"
    # "publishing"
    # "marketing"
)

SCAN_TYPE="${1:-all}"

for PLATFORM in "${PLATFORMS[@]}"; do
    echo -e "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
    echo -e "${BLUE}🔍 Scanning: $PLATFORM${NC}"
    echo -e "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
    echo ""
    
    "$SCRIPT_DIR/scan-platform.sh" "$PLATFORM" "$SCAN_TYPE"
    
    echo ""
done

# Generate combined summary
echo -e "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
echo -e "${BLUE}📊 Generating Combined Summary${NC}"
echo -e "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
echo ""

COMBINED_SUMMARY="$PROJECT_ROOT/scan-results/COMBINED_SUMMARY.md"

cat > "$COMBINED_SUMMARY" <<EOF
# Combined Scan Summary: All Platforms

**Date:** $(date)

---

EOF

for PLATFORM in "${PLATFORMS[@]}"; do
    if [ -f "$PROJECT_ROOT/scan-results/$PLATFORM/summary.md" ]; then
        echo "## $PLATFORM" >> "$COMBINED_SUMMARY"
        echo "" >> "$COMBINED_SUMMARY"
        cat "$PROJECT_ROOT/scan-results/$PLATFORM/summary.md" >> "$COMBINED_SUMMARY"
        echo "" >> "$COMBINED_SUMMARY"
        echo "---" >> "$COMBINED_SUMMARY"
        echo "" >> "$COMBINED_SUMMARY"
    fi
done

echo -e "${GREEN}✅ Combined summary: $COMBINED_SUMMARY${NC}"
echo ""

echo -e "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
echo -e "${GREEN}✅ All Platform Scans Complete!${NC}"
echo -e "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
echo ""
echo "📊 Individual results in: scan-results/<platform>/"
echo "📝 Combined summary: $COMBINED_SUMMARY"
echo ""
